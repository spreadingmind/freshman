import sys

class Node:
    def __init__(self,data):
        self.right=self.left=None
        self.data = data
class Solution:
    def insert(self,root,data):
        if root==None:
            return Node(data)
        else:
            if data<=root.data:
                cur=self.insert(root.left,data)
                root.left=cur
            else:
                cur=self.insert(root.right,data)
                root.right=cur
        return root

    def levelOrder(self, root):
        queue = []
        if root is not None:
            queue.append(root)
        while queue:
            x = queue.pop(0)
            print (x.data, end=' ')
            if x.left:
                queue.append(x.left)
            if x.right:
                queue.append(x.right)
        return None

